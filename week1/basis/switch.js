const color = "purple";

switch (color) {
  case "blue":
    console.log("De kleur is blauw");
    break;
  case "orange":
    console.log("De kleur is oranje");
    break;
  case "red":
    console.log("De kleur is rood");
    break;
  case "yellow":
    console.log("De kleur is geel");
    break;
  default:
    console.log("We kennen de kleur niet");
    break;
}
